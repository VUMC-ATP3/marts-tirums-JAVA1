package clasroomFive.polymorphism;

public class Calculator {

    public int sum(int a, int b){
        return a + b;
    }

    public double sum (double a, double b){
        return a + b;
    }

    public int sum (int a, int b, int c){
        return a + b + c;
    }

    public double sum (int i, int i1, int i2, double v){
        return i + i1 + i2 + v;
    }

}
